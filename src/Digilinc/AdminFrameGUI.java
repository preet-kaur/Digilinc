    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
       if((txtchangepasspass.getText().equals(""))||(txtchangepasspass.getText().equals(""))||(txtchangepassusrname.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null,"Please fill the details");
        }
        else
        {
            int lid=0;
            try
            {
                stmt = conn.createStatement();
                String query = "Select loginid from login where username='"+txtchangepassusrname.getText()+"' and password='"+txtchangepasspass.getText()+"'";
                rst = stmt.executeQuery(query);
                while(rst.next()){
                    lid = rst.getInt("loginid");
                }
                if(lid ==0 )
                {
                    JOptionPane.showMessageDialog(null, "Username Password Combination Invalid");
                }
                else
                {
                    query = "Update login set password='"+txtchangepassnewpass.getText()+"' where loginid="+lid;
                    stmt.executeUpdate(query);
                    JOptionPane.showMessageDialog(null, "Password changed");
                }
            }
            catch(SQLException ex)
            {
                Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtchangepassnewpass.setText("");
            txtchangepasspass.setText("");
            txtchangepassusrname.setText("");
        }
    }//GEN-LAST:event_btn_updateActionPerformed
