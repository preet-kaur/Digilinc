    private void btndocsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndocsaveActionPerformed
        if((txtdoc.getText()==null) ||(txtmob.getText() == null) ||(txtemail.getText()==null)
            ||(txtspec.getText() == null)||(txtaddress.getText() == null)||(cmbdegree.getSelectedIndex()==-1))
        {
            JOptionPane.showMessageDialog(null, "Fill in all the details");
        }
        else
        {
            String docname = txtdoc.getText();
            String mob = txtmob.getText();
            String email = txtemail.getText();
            String degree = cmbdegree.getSelectedItem().toString();
            String add = txtaddress.getText();
            String spec = txtspec.getText();
            try
            {
                System.out.println(docname);
                pst = conn.prepareStatement("select name from doctor where name = ?");
                pst.setString(1,docname);
                rst = pst.executeQuery();
                if(rst.next())
                {
                    System.out.println(rst.getString("name"));
                    pst = conn.prepareStatement("update doctor set name = ? , contact=? , email=? , degree=?"
                        + ", special=?, address=? where name=?");
                    pst.setString(1, docname);
                    pst.setString(2, mob);
                    pst.setString(3, email);
                    pst.setString(4, degree);                 
                    pst.setString(5, spec);
                    pst.setString(6,add);
                    pst.setString(7,docname);
                    int i= pst.executeUpdate();
                    if(i>0)
                    {
                        JOptionPane.showMessageDialog(null, "Doctor Updated Successfully");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Error adding Doctor");
                    }
                }
                else  //if no doctor with such name then insert new doctor
                {
                    pst = conn.prepareStatement("insert into doctor(name,contact,email,degree,special,address) values (?,?,?,?,?,?)");
                    pst.setString(1, docname);
                    pst.setString(2, mob);
                    pst.setString(3, email);
                    pst.setString(4, degree);
                    pst.setString(5, spec);
                    pst.setString(6,add);
                    int i= pst.executeUpdate();
                    if(i>0)
                    {
                        JOptionPane.showMessageDialog(null, "Doctor Added Successfully");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Error adding Doctor");
                    }
                }

            }
            catch(SQLException e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btndocsaveActionPerformed

