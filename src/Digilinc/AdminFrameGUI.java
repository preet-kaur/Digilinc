    public void addprescriptions()
    {
        String presname = txtppname.getText();
        String purpose = cmbdisease.getSelectedItem().toString();
        String output=null;
        try
        {
            pst = conn.prepareStatement("select name from medicine where name = ? and disease = ?");
            pst.setString(1, presname);
            pst.setString(2, purpose);
            rst = pst.executeQuery();
            while(rst.next())
            {
                output = rst.getString("name");
            }
            if(output !=null)
            {
                JOptionPane.showMessageDialog(null, "Prescription already added");
            }
            else
            {
                pst = conn.prepareStatement("insert into medicine(name,disease) values (?,?)");
                pst.setString(1, presname);
                pst.setString(2, purpose);
                int i = pst.executeUpdate();
                if (i>0)
                {
                    JOptionPane.showMessageDialog(null,"Medicine added successfully ");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Error while adding medicine ");
                }
            }
        }
        catch(Exception e)
        {
            System.out.println("Error at add prescription: "+e);
        }
    }
    
